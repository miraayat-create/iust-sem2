#include<stdio.h>
int main(void)
{
    int a = 10; int b = 4;
    // Relational Operators
    printf("Relational Operators:\n");
    printf("a == b: %d\n", a == b);
    printf("a != b: %d\n", a != b);
    printf("a > b: %d\n", a > b);
    printf("a >= b: %d\n", a >= b);
    printf("a <= b: %d\n\n", a <= b);
}